def somar(x, y):
    return x + y

def subtrair(x, y):
    return x - y

def dividir(x, y):
    if x != 0 and y != 0: #forma de divisão
        return x / y
    return  0

def multiplicar(x, y):
    return x * y

def resto(x, y):
    return x % y #usamos % para saber se o número é divisivel
